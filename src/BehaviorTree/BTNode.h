#pragma once

/**
 * @brief Прямое (предварительное) объявление класса BTContext.
 * @details Мы "обещаем" компилятору, что такой класс существует,
 *          не раскрывая его полного содержимого. Это позволяет нам
 *          использовать указатели и ссылки (BTContext&) в сигнатуре
 *          метода tick(), разрывая при этом циклическую зависимость
 *          заголовков. Полное определение BTContext будет подключено
 *          в .cpp файлах конкретных узлов, которым нужен доступ
 *          к его полям.
 */
class BTContext;

namespace bt
{
    /**
     * @brief Возможные статусы завершения работы узла.
     */
    enum class NodeStatus
    {
        Success, // Узел успешно выполнил свою задачу.
        Failure, // Узел не смог выполнить свою задачу.
        Running  // Узел еще в процессе выполнения (например, движение к точке).
    };

    /**
     * @brief Абстрактный базовый класс для всех узлов в дереве поведения.
     */
    class BTNode
    {
    public:
        virtual ~BTNode() = default;

        /**
         * @brief Основной метод, который "тикает" в каждом цикле.
         *        Вся логика узла находится здесь.
         * @param context Контекст с доступом ко всем системам бота.
         * @return Статус выполнения узла.
         */
        virtual NodeStatus tick(BTContext &context) = 0;
    };
} // namespace bt